    private String getSelectedButtonText(ButtonGroup buttonGroup) {
        for (var enumeration = buttonGroup.getElements(); enumeration.hasMoreElements();) {
            AbstractButton button = enumeration.nextElement();
            if (button.isSelected()) {
                return button.getText();
            }
        }
        return null;
    }
    
    public boolean comprobarCedula(String cedula) {
        // Verificar que la cédula tenga exactamente 10 dígitos
        if (cedula == null || cedula.length() != 10) {
            JOptionPane.showMessageDialog(rootPane, "Cedula incorrecta, son 10 dígitos");
            return false;
        }

        try {
            int provincia = Integer.parseInt(cedula.substring(0, 2));
            int digitoVerificador = Integer.parseInt(cedula.substring(9, 10));

            // Verificar que los primeros dos dígitos (provincia) estén en el rango 00-24
            if (provincia < 0 || provincia > 24) {
                JOptionPane.showMessageDialog(rootPane, "Cedula incorrecta");
                return false;
            }

            int[] coeficientes = {2, 1, 2, 1, 2, 1, 2, 1, 2};
            int suma = 0;

            // Calcular la suma de los productos según los coeficientes
            for (int i = 0; i < 9; i++) {
                int digito = Integer.parseInt(cedula.substring(i, i + 1));
                int producto = digito * coeficientes[i];
                suma += (producto >= 10) ? producto - 9 : producto;
            }

            // Calcular el dígito verificador
            int digitoCalculado = (suma % 10 == 0) ? 0 : 10 - (suma % 10);

            if(digitoVerificador == digitoCalculado){
                return true;
            }else{
                JOptionPane.showMessageDialog(rootPane, "Cedula incorrecta");
                txtCedula.setText("");
                return false;
            }

        } catch (NumberFormatException e) {
            // La cédula contiene caracteres no numéricos
            JOptionPane.showMessageDialog(rootPane, "Cedula incorrecta, ingrese solo números");
            return false;
        }
    }
    
    public void selectGender(String gender) {
        if (gender.equalsIgnoreCase("Masculino")) {
            masculino.setSelected(true);
        } else if (gender.equalsIgnoreCase("Femenino")) {
            femenino.setSelected(true);
        }
    }
    
    private void borrarCampos(){
        txtApellido.setText("");
        txtCedula.setText("");
        txtCorreo.setText("");
        txtFecha.setText("");
        txtNombre.setText("");
        generos.clearSelection();
    }
    
    private void cambiarEditable(boolean editable){
        txtApellido.setEditable(editable);
        txtCorreo.setEditable(editable);
        txtFecha.setEditable(editable);
        txtNombre.setEditable(editable);
        masculino.setEnabled(editable);
        femenino.setEnabled(editable);
    }
    
    private boolean comprobarCampos(){
        if(txtApellido.getText().equals("") || txtCedula.getText().equals("") || txtCorreo.getText().equals("") || txtFecha.getText().equals("") || txtNombre.getText().equals("") || (femenino.isSelected()== false && masculino.isSelected()== false)){
            JOptionPane.showMessageDialog(rootPane, "Llene todos los campos");
            return false;
        }else{
            return true;
        }
    }

