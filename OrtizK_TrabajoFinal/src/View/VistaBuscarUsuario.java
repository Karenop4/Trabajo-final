    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        
        if(txtCedula.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "No se ha ingresado ninguna cédula");
        }else{
            if(comprobarCedula(txtCedula.getText())){
                pacienteEncontrado = true;
                rs = personaControlador.buscarPaciente(txtCedula.getText());
                
                try {
                    if (rs.next()) { // Verifica si hay al menos una fila de resultados
                        txtApellido.setText(rs.getString("apellido"));
                        txtCorreo.setText(rs.getString("email"));
                        txtFecha.setText(rs.getString("fechaNacimiento"));
                        txtNombre.setText(rs.getString("nombre"));
                        int codigoGenero = rs.getInt("codigoGenero");

                        selectGender(personaControlador.devolverGenero(codigoGenero));
                    }else{
                        JOptionPane.showMessageDialog(rootPane, "No se encontró ningun paciente");
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(VistaBuscarUsuario.class.getName()).log(Level.SEVERE, null, ex);
                }


            }else{
                txtCedula.setText("");
            }
        }
        
        
        
    }//GEN-LAST:event_btnBuscarActionPerformed

